package com.github.narh.sample.mybatis;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.github.narh.sample.mybatis.domain.model.User;

/**
 * 認証会員情報
 *
 * @author NARH https://github.com/NARH
 *
 */
public class AccountDetails implements UserDetails {

  /** ログイン情報*/
  private User user;
  /** 権限情報*/
  private List<GrantedAuthority> authorities = new ArrayList<>();

  /**
   * コンストラクタ
   *
   * @param user UserMapper で取得したログイン情報
   */
  public AccountDetails(final User user) {
    this.user = user;
    /* 権限情報を登録*/
    user.getAuthorities().forEach(a->{
      authorities.add(new SimpleGrantedAuthority(a.getAuthority()));
    });
  }

  @Override
  public Collection<? extends GrantedAuthority> getAuthorities() {
    return authorities;
  }

  @Override
  public String getPassword() {
    return user.getPassword();
  }

  @Override
  public String getUsername() {
    return user.getUsername();
  }

  @Override
  public boolean isAccountNonExpired() {
    return true;
  }

  @Override
  public boolean isAccountNonLocked() {
    return true;
  }

  @Override
  public boolean isCredentialsNonExpired() {
    return true;
  }

  @Override
  public boolean isEnabled() {
    return true;
  }

  public User getUser() {
    return user;
  }
}
